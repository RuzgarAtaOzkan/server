  async get_settings(credentials: any): Promise<object | null> {
    const settings: string = await this.options.redis.get('settings');
    const result: object = JSON.parse(settings);
    return result;
  }

  async edit_settings(credentials: any): Promise<any> {
    await this.validator.edit_settings(credentials);

    const settings: any | null = JSON.parse(
      await this.options.redis.get('settings')
    );

    await this.options.redis.set('settings', JSON.stringify(settings));

    return settings;
  }

  get_cities(credentials: any): any[] {
    return this.cities;
  }

  get_districts(credentials: any) {
    const index: number = Number(credentials.city) - 1;
    return this.districts[index];
  }

  get_neighbourhoods(credentials: any) {
    const index: number = Number(credentials.district) - 1;
    return this.neighbourhoods[index];
  }
